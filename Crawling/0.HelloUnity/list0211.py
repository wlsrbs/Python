# 두 개의 정수를 입력받아 사칙연산 수행하기 1(문자열을 입력받아 정수로 변환)

s = input('정수 a：')
a = int(s)
s = input('정수 b：')
b = int(s)

print('a + b는',  a + b,  '입니다.')
print('a - b는',  a - b,  '입니다.')
print('a * b는',  a * b,  '입니다.')
print('a / b는',  a / b,  '입니다.')
print('a // b는', a // b, '입니다.')
print('a % b는',  a % b,  '입니다.')
print('a ** b는', a ** b, '입니다.')
