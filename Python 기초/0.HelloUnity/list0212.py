# 두 개의 정수를 입력받아 사칙연산 수행하기 2(입력과 변환을 단일 문으로 수행）

a = int(input('정수 a：'))
b = int(input('정수 b：'))

print('a + b는',  a + b,  '입니다.')
print('a - b는',  a - b,  '입니다.')
print('a * b는',  a * b,  '입니다.')
print('a / b는',  a / b,  '입니다.')
print('a // b는', a // b, '입니다.')
print('a % b는',  a % b,  '입니다.')
print('a ** b는', a ** b, '입니다.')
