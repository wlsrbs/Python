# 두 개의 정수를 입력받아 사칙연산 수행하기 4(format 이용하기）

a = int(input('정수 a：'))
b = int(input('정수 b：'))

print('a + b는 {}입니다.'.format(a + b))
print('a - b는 {}입니다.'.format(a - b))
print('a * b는 {}입니다.'.format(a * b))
print('a / b는 {}입니다.'.format(a / b))
print('a // b는 {}입니다.'.format(a // b))
print('a % b는 {}입니다.'.format(a % b))
print('a ** b는 {}입니다.'.format(a ** b))
